#Latihan Mandiri 5.1

def perkalian(bil1, bil2):
    hasil = 0
    print(f"{bil2} x {bil1} = ", end="")
    for i in range(bil2):
        hasil += bil1
        if i < bil2 - 1:
            print(f"{bil1} + ", end="")
        else:
            print(f"{bil1} = ", end="")
    return hasil

bil1 = int(input("Masukkan bilangan yang dikali : "))
bil2 = int(input("Masukkan bilangan perkalian : "))
print(perkalian(bil1, bil2))

