def ganjil(bil1, bil2):
    if bil2 > bil1 :
        while bil2 != bil1 :
            if bil2 % 2 == 1 :
                print(bil2, end=",")
            bil2 = bil2 - 1
    else :
        while bil2 != bil1 :
            bil2 = bil2 + 1
            if bil2 % 2 == 1 :
                print(bil2, end=",")

bawah = int(input("Masukkan nilai bawah : "))
atas = int(input("Masukkan nilai atas : "))
ganjil(atas, bawah)

