def cekprima(a, b = None):
    if a <= 1:
        print("Bukan bilangan prima")
        return False
    if b is None:
        b = a - 1
    if b < 2:
        print("Ini bilangan prima")
        return True
    if a % b == 0:
        print("Bukan bilangan prima")
        return False
    return cekprima(a, b - 1)

