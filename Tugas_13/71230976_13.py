def hanya_huruf(s):
    return ''.join(char.lower() for char in s if char.isalpha())

def palindrom(s):
    if len(s) < 1:
        return True
    else:
        if s[0] == s[-1]:
            return palindrom(s[1:-1])
        else:
            return False

s = input("Masukkan kata: ")
s = hanya_huruf(s)
if palindrom(s):
    print("Palindrom")
else:
    print("Bukan Palindrom")

