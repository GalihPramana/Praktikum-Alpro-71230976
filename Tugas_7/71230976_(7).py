def cek_kata(kalimat):
    kata = kalimat.split()
    terpendek = min(kata, key=len)
    terpanjang = max(kata, key=len)
    print("Kata terpendek:", terpendek)
    print("Kata terpanjang:", terpanjang)

kalimat = input("Masukkan kalimat: ")
cek_kata(kalimat)

