def hitung_frekuensi(kalimat, kata) :
    kalimat = kalimat.lower()
    kata = kata.lower()
    hasil = kalimat.count(kata)
    print(f'{kata} ada {hasil} buah.')

kalimat = input("masukkan kalimat : ")
kata = input("masukkan kata : ")

hitung_frekuensi(kalimat, kata)

