def hapus_spasi(kalimat):
    kalimat_tanpa_spasi = " ".join(kalimat.split())
    return kalimat_tanpa_spasi

kalimat_input = input("Masukkan kalimat dengan spasi yang ingin dihapus: ")
print(hapus_spasi(kalimat_input))

